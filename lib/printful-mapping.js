// 2-letter country code helpers — Printful is strict about ISO 3166-1 alpha-2.
// Stripe Checkout's allowed_countries already returns codes in this format,
// so this is mostly a safety net.
function normalizeCountryCode(input) {
  if (!input) return null;
  const c = String(input).trim().toUpperCase();
  if (c.length === 2) return c;
  const fallbacks = {
    'UNITED STATES': 'US',
    'CANADA': 'CA',
    'UNITED KINGDOM': 'GB',
    'AUSTRALIA': 'AU',
  };
  return fallbacks[c] || null;
}

